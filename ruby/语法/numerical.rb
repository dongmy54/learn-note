#===================================================================================#
# abs 绝对值
2.3.abs   # => 2.3
-2.3.abs  # => 2.3


#===================================================================================#
# round 四舍五入 保留位数
puts 2.round(2)       # => 2.0      整数 round后 补一位
puts 3.6254.round     # => 4        默认 保留 0位
puts 3.6254.round(2)  # => 3.63     
puts 123.round(-2)    # => 100      参数 为负 收敛整数部分


#===================================================================================#
# ceil 往大数 方向变整数
puts 2.ceil           # => 2
puts 2.3.ceil         # => 3
puts 2.5.ceil         # => 3
puts -2.6.ceil        # => -2 


#===================================================================================#
# floor 往小数 方向变整数
puts 3.floor          # => 3
puts 3.3.floor        # => 3
puts 3.9.floor        # => 3
puts -2.1.floor       # => -3


#===================================================================================#
# % 格式化输出
"%05d" % 23  # 总共保持五位,左侧缺失补0
# => "00023"
"%0.2f" % 23.456 # 保留多少小数，这里会四舍五入
# => 23.46


#===================================================================================#
# divmod 求 商 求 余
# PS： 推荐用于整数
11.divmod(3)
# => [
# =>     [0] 3,
# =>     [1] 2
# => ]
12.4.divmod(3)
# => [
# =>     [0] 4,
# =>     [1] 0.40000000000000036
# => ]


#===================================================================================#
# step 按照 进度 改变值
# PS: 推荐用于整数
3.step(by: 0.5).take(3)
# => [
# =>     [0] 3.0,
# =>     [1] 3.5,
# =>     [2] 4.0
# => ]
3.2.step(by: 0.3).take(3)
# => [
# =>     [0] 3.2,
# =>     [1] 3.5,
# =>     [2] 3.8000000000000003
# => ]